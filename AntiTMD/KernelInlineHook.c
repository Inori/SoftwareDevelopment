ULONG GetPatchSize(PUCHAR pInstAddr)
{
	ULONG nTotalLen = 0, nInstLen = 0;

	while (nTotalLen < JMP_INST_LEN)	//ÖÁÉÙÐèÒªJMP_INST_LEN×Ö½Ú
	{
		nInstLen = LDE(pInstAddr, 0);
		pInstAddr += nInstLen;
		nTotalLen += nInstLen;
	}

	return nTotalLen;
}

